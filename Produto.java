package prod;

public class Produto {
	private String descricao;
	private String marca;
	private double custo;
	private double venda;
	private int quantidade;
	private double tcusto;
	private double tvenda;
	private int tquantidade;
		
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public double getCusto() {
		return custo;
	}
	public void setCusto(double custo) {
		this.custo = custo;
	}
	public double getVenda() {
		return venda;
	}
	public void setVenda(double venda) {
		this.venda = venda;
	}
	public int getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	public double getTcusto() {
		return tcusto;
	}
	public void setTcusto(double tcusto) {
		this.tcusto = tcusto;
	}
	public double getTvenda() {
		return tvenda;
	}
	public void setTvenda(double tvenda) {
		this.tvenda = tvenda;
	}
	public int getTquantidade() {
		return tquantidade;
	}
	public void setTquantidade(int tquantidade) {
		this.tquantidade = tquantidade;
	}
	
		
}

